#Ejercicio 2. área y Perimetro

#Entradas de datos
#Declarar o creación de variables
π = 3.1416
r = float(input("Escribe Radio: "))


# PROCESOS
Périmetro = ((2*π) * r)
Área = (π * (pow(r,2)))


#Salida de procesos
print("El Périmeto es:", round(Périmetro,2))
print("El Áera es:", round(Área,2))
